def phan_tich_to_hop_sao(la_so_dict):
    all_stars = {}
    for cung in la_so_dict["thap_nhi_cung"]:
        sao_list = []
        for s in cung["chinh_tinh"]:
            sao_list.append(s.split("(")[0])
        for s in cung["phu_tinh"]:
            sao_list.append(s)
        for s in cung["sat_tinh"]:
            sao_list.append(s)
        all_stars[cung["so_thu_tu"]] = sao_list

    all_stars_set = set()
    for saos in all_stars.values():
        for s in saos:
            all_stars_set.add(s)

    all_stars_list = list(all_stars_set)
    menh_stars = all_stars.get(la_so_dict["thap_nhi_cung"][0]["so_thu_tu"], [])

    ket_qua = []
    for key, info in STAR_COMBOS.items():
        parts = [p.strip() for p in key.split("+")]
        matched = True
        for part in parts:
            if "(" in part:
                continue
            found = False
            for s in all_stars_list:
                if s.startswith(part.split("(")[0].strip()):
                    found = True
                    break
            if not found:
                matched = False
                break
        if matched:
            entry = {
                "ten": info["ten"],
                "mo_ta": info["mo_ta"],
                "y_nghia": info["y_nghia"],
            }
            if "anh_huong" in info:
                entry["anh_huong"] = info["anh_huong"]
            ket_qua.append(entry)

    return ket_qua
