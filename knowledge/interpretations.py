CUNG_KEY_MAP = {
    "Mệnh": "Menh", "Huynh đệ": "Huynh de", "Phu thê": "Phu the", "Tử tức": "Tu tuc",
    "Tài bạch": "Tai bach", "Tật ách": "Tat ach", "Thiên di": "Thien di", "Nô bộc": "No boc",
    "Quan lộc": "Quan loc", "Điền trạch": "Dien trach", "Phúc đức": "Phuc duc", "Phụ mẫu": "Phu mau",
}


def normalize_cung_name(ten_cung):
    if ten_cung in CUNG_KEY_MAP:
        return CUNG_KEY_MAP[ten_cung]
    for k, v in CUNG_KEY_MAP.items():
        if k.lower() == ten_cung.lower():
            return v
    return ten_cung


def giai_doan_cung(ten_cung, chinh_tinh, phu_tinh, sat_tinh, dac_tinh):
    key = normalize_cung_name(ten_cung)
    data = PALACE_INTERPRETATIONS.get(key)
    if not data:
        return {"ten": ten_cung, "mo_ta": "Chưa có dữ liệu giải đoán"}

    ket_qua = {
        "ten": ten_cung,
        "tieu_de": data["title"],
        "tong_quan": data["general"],
        "sao_phan_tich": [],
    }

    VN_TO_ASCII = {
        "tử vi": "tu vi", "thiên cơ": "thien co", "thái dương": "thai duong",
        "vũ khúc": "vu khuc", "thiên đồng": "thien dong", "liêm trinh": "liem trinh",
        "thiên phủ": "thien phu", "thái âm": "thai am", "tham lang": "tham lang",
        "cự môn": "cu mon", "thiên tướng": "thien tuong", "thiên lương": "thien luong",
        "thất sát": "that sat", "phá quân": "pha quan",
    }

    def normalize_star_name(name):
        name = name.replace(" ", "").lower()
        for vn, ascii in VN_TO_ASCII.items():
            vn_clean = vn.replace(" ", "").lower()
            if vn_clean == name or vn_clean in name or name in vn_clean:
                return ascii.replace(" ", "").lower()
        return name

    star_keys_lookup = {}
    for orig_key in data["stars"]:
        star_keys_lookup[normalize_star_name(orig_key)] = orig_key

    for sao in chinh_tinh:
        sao_goc = sao.split("(")[0]
        ns = normalize_star_name(sao_goc)
        best_key = None
        for sk, orig in star_keys_lookup.items():
            if sk == ns or (len(sk) >= 4 and (sk in ns or ns in sk)):
                best_key = orig
                break
        if best_key is None:
            for sk, orig in star_keys_lookup.items():
                if len(sk) >= 4 and sk[:4] == ns[:4]:
                    best_key = orig
                    break
        if best_key:
            sao_info = {"sao": sao, "giai_doan": data["stars"][best_key]}
            if sao in dac_tinh:
                sao_info["dac_tinh"] = dac_tinh[sao]
            ket_qua["sao_phan_tich"].append(sao_info)

    ket_qua["phu_tinh"] = phu_tinh
    ket_qua["sat_tinh"] = sat_tinh

    return ket_qua


def giai_doan_toan_bo(la_so_dict):
    ket_qua = []
    for cung in la_so_dict["thap_nhi_cung"]:
        gd = giai_doan_cung(cung["ten"], cung["chinh_tinh"], cung["phu_tinh"], cung["sat_tinh"], cung.get("dac_tinh", {}))
        ket_qua.append(gd)
    return ket_qua
