def giai_thich_tu_hoa(hoa_loc=None, hoa_quyen=None, hoa_khoa=None, hoa_ky=None):
    result = []
    if hoa_loc:
        info = BAC_PHAI_KNOWLEDGE["tu_hoa"]["cac_sao"]["Hóa Lộc (A)"]
        result.append(f"Hóa Lộc ({hoa_loc}): {info['y_nghia']}. Tính chất: {info['tinh_chat']}.")
    if hoa_quyen:
        info = BAC_PHAI_KNOWLEDGE["tu_hoa"]["cac_sao"]["Hóa Quyền (B)"]
        result.append(f"Hóa Quyền ({hoa_quyen}): {info['y_nghia']}. Tính chất: {info['tinh_chat']}.")
    if hoa_khoa:
        info = BAC_PHAI_KNOWLEDGE["tu_hoa"]["cac_sao"]["Hóa Khoa (C)"]
        result.append(f"Hóa Khoa ({hoa_khoa}): {info['y_nghia']}. Tính chất: {info['tinh_chat']}.")
    if hoa_ky:
        info = BAC_PHAI_KNOWLEDGE["tu_hoa"]["cac_sao"]["Hóa Kỵ (D)"]
        result.append(f"Hóa Kỵ ({hoa_ky}): {info['y_nghia']}. Tính chất: {info['tinh_chat']}.")
    return result


def xac_dinh_lai_nhan_cung(dia_chi_nam_sinh):
    cung_map = {
        "Tý": "Mệnh", "Sửu": "Huynh Đệ", "Dần": "Phu Thê", "Mão": "Tử Tức",
        "Thìn": "Tài Bạch", "Tỵ": "Tật Ách", "Ngọ": "Thiên Di", "Mùi": "Nô Bộc",
        "Thân": "Quan Lộc", "Dậu": "Điền Trạch", "Tuất": "Phúc Đức", "Hợi": "Phụ Mẫu"
    }
    return cung_map.get(dia_chi_nam_sinh, "Không xác định")


def phan_tich_nga_tha_cung(cung_ten):
    nga_cung = ["Mệnh", "Tài Bạch", "Quan Lộc", "Tật Ách", "Phúc Đức", "Điền Trạch"]
    return "ngã" if cung_ten in nga_cung else "tha"


def tra_cuu_tu_hoa_thap_can(thien_can):
    return BAC_PHAI_KNOWLEDGE["bang_tu_hoa"]["bang"].get(thien_can)


def tra_cuu_sao_nam_nu(ten_sao):
    if ten_sao in BAC_PHAI_KNOWLEDGE["sao_nam_sao_nu"]["sao_nam"]:
        return "Nam"
    if ten_sao in BAC_PHAI_KNOWLEDGE["sao_nam_sao_nu"]["sao_nu"]:
        return "Nữ"
    return "Không xác định"
