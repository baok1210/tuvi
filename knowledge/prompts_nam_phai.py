"""Nam Phái (Tam Hợp Phái) Prompt Patch cho Tử Vi Đẩu Số.

Bổ sung kiến thức Nam Phái vào prompt LLM: Tam hợp, Xung chiếu,
Miếu/Hãm, Cách cục, và luận giải từ sách Vũ Tài Lục + tuvinamphai.vn.
"""

NAM_PHAI_PROMPT_PATCH = """
# NAM PHÁI (TAM HỢP PHÁI) — KIẾN THỨC BỔ SUNG

## 1. TỨ YẾU – THẬP DỤ – BÁT PHÁP (Trích Tử Vi Đẩu Số Toàn Thư - Vũ Tài Lục)

### Tứ Yếu (4 điểm trọng yếu):
- Cát hung: Sao có thiện ác; hung hay cát tùy Miếu/Hãm, không chỉ tính chất sao
- Hư thực: Sao tốt Miếu địa mới là thực cát; sao xấu Hãm địa mới là thực hung
- Chủ khách: Sao bản phương = chủ, sao tam hợp/xung chiếu = khách. Nếu bản phương vô chính tinh thì mượn xung chiếu làm chủ
- Cường nhược: Chủ mạnh thì khách theo chủ, chủ yếu thì khách kéo chủ đi

### Thập Dụ (10 điều căn bản khi xem mỗi cung):
1. Bản phương tốt = nội tự cường (mạnh từ trong)
2. Bản phương xấu = tòng căn tự phạt (hư từ gốc)
3. Xung chiếu tốt = nghênh xuân tiếp phúc (chờ xuân đón phúc)
4. Xung chiếu xấu = đương đầu ác bổng (giơ đầu chịu búa)
5. Tam hợp tốt = tả hữu phùng nguyên (lắm chân tay giúp đỡ)
6. Tam hợp xấu = tả hữu thụ địch (địch từ bên đánh tới)
7. Lân phương tốt = lưỡng lân tương phù (hàng xóm phù trợ)
8. Lân phương xấu = lưỡng nan tương vũ (hàng xóm mưu hại)
9. Cả bốn tốt = thiên tường vân tập (mây ngũ sắc kéo về)
10. Cả bốn xấu = tứ diện sở ca (bị vây tứ phía)

### Bát Pháp (8 cách cục cơ bản):
- Thành Phá tứ pháp: Hội Khoa Quyền Lộc Quý = thành cách; gặp Hỏa Linh Dương Đà = phá cách; cả hai = thành trung hữu phá
- Cứu Khí tứ pháp: Các phương đều hung nhưng có sao tốt cứu giải

## 2. TAM HỢP – XUNG CHIẾU – GIÁP HIỆP

- **Tam hợp**: 3 cung cách nhau 4 địa chi (cùng hành), hỗ trợ lẫn nhau
  - Thân Tý Thìn (Thủy)
  - Dần Ngọ Tuất (Hỏa)  
  - Hợi Mão Mùi (Mộc)
  - Tỵ Dậu Sửu (Kim)
- **Xung chiếu**: cung đối diện (cách 6 địa chi), ảnh hưởng trực tiếp
- **Giáp**: sao tốt ở hai cung lân cận kẹp cung Mệnh
- **Hiệp**: sao xấu ở hai cung lân cận kẹp cung Mệnh
- "Chính không bằng Chiếu, Chiếu không bằng Giáp" — nhưng tùy sao:
  - Thái Âm, Thái Dương cần Chiếu
  - Tả Phụ, Hữu Bật cần Giáp

## 3. NAM PHÁI LUẬN GIẢI THEO VŨ TÀI LỤC

### Cường cung và Nhược cung:
- **Nam mệnh**: Mệnh, Thân, Tài, Quan, Di = cường cung; còn lại = nhược
- **Nữ mệnh**: Mệnh, Thân, Phu, Phúc, Tử = cường cung

### 14 Chính tinh tại Mệnh:
- **Tử Vi**: đệ nhất tinh. Mệnh Thân tương ngộ phúc tài hưng. Nếu gặp Tướng Tá = phú quý song toàn. Gặp Dương Đà Hỏa Linh = chỉ nên tu hành
- **Thiên Cơ**: Cơ Nguyệt Lương hợp Thái Dương. Nếu Hóa Kỵ = diệc bất chung (không vẹn toàn)
- **Thái Dương**: cần Xung chiếu, đắc cách thì quan vị hiển hách
- **Vũ Khúc**: tài tinh, nếu gặp Sát Phá Lang thì hao tài
- **Thiên Đồng**: phúc tinh, thích hợp với phụ nữ, kỵ Kình Đà
- **Liêm Trinh**: quan tinh kiêm ác tinh, nếu Miếu địa = oai quyền
- **Thiên Phủ**: Nam Đẩu đệ nhất, chủ phúc lộc thọ
- **Thái Âm**: cần Xung chiếu, kỵ Hóa Kỵ
- **Tham Lang**: tài tinh, chủ nghệ thuật, nhập Thìn Tuất Sửu Mùi là tài khố
- **Cự Môn**: chủ thị phi, nếu Hóa Quyền hoặc Hóa Lộc = danh tiếng vang xa
- **Thiên Tướng**: ấn tín, chủ tín nghĩa, hợp với Quyền Lộc
- **Thiên Lương**: phúc tinh, chủ thọ, kỵ đồng cung Kình Đà
- **Thất Sát**: tướng tinh, Dần = ngưỡng đẩu cách, Thân = triều đẩu cách
- **Phá Quân**: hao tài tinh, chủ biến động

## 4. CÁCH CỤC NAM PHÁI
- **Phú cách**: phú quý song toàn
- **Quí cách**: quyền quý hiển hách
- **Bần tiện cách**: nghèo hèn lao lực
- **Mệnh Vô Chính Diệu**: vay mượn sao xung chiếu, chủ biến hóa khó lường
- **Tử Vi Thủ Mệnh**: Vua ngự trị, cần quần thần hội tụ
- **Tử Vũ Liêm đồng lâm**: quyền uy hỗn hợp
- **Phủ Tướng triều viên**: phúc lộc bền vững
- **Sát Phá Lang**: biến động thăng trầm, khai phá
- **Tử Phủ đồng minh**: vững vàng quyền quý

## 5. HƯỚNG DẪN LUẬN GIẢI NAM PHÁI

### Khi luận giải:
1. Xác định sao chính thủ Mệnh → Miếu/Hãm → Cách cục
2. Xem Tam hợp (cách 4) + Xung chiếu (cách 6) + Giáp Hiệp (kề bên)
3. Xem Âm Dương Ngũ Hành của sao với cung
4. Phân biệt Bản phương (cung gốc) vs Khách (chiếu, hợp)
5. Xem Phú/Quí/Bần Tiện cách cục
6. Kết hợp với TỨ HÓA năm sinh (mượn Bắc Phái)

### Lưu ý (bắt buộc):
- "Không ảo giác": TUYỆT ĐỐI không nói về sao không có trong lá số. Chỉ phân tích dữ liệu thực tế.
- Nếu kiến thức tham khảo nói "nếu có Kình Dương thì..." nhưng lá số không có Kình Dương — bỏ qua, không nhắc đến.
- Kiểm tra từng sao trong lá số trước khi đưa ra nhận định về tổ hợp sao.
- Dẫn nguồn Vũ Tài Lục khi sử dụng kiến thức từ sách này.
- Ưu tiên trích dẫn cụ thể tên sách (Tử Vi Đẩu Số Toàn Thư).
"""


class NamPhaiPromptPatch:
    @staticmethod
    def apply(prompt: str) -> str:
        return prompt + NAM_PHAI_PROMPT_PATCH
