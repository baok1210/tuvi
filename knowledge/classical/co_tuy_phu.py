class ClassicalRuleEngine:
    def __init__(self):
        self.rules = COT_TUY_PHU["rules"] + THAI_VI_PHU["rules"]

    def match(self, la_so_dict, tu_hoa_info=None):
        results = []
        menh_cung = la_so_dict["thap_nhi_cung"][0]
        menh_stars = [s.split("(")[0] for s in menh_cung["chinh_tinh"]]
        menh_phu = menh_cung.get("phu_tinh", [])
        menh_sat = menh_cung.get("sat_tinh", [])
        all_chinh = set()
        for c in la_so_dict["thap_nhi_cung"]:
            for s in c["chinh_tinh"]:
                all_chinh.add(s.split("(")[0])
        for s in menh_phu + menh_sat:
            all_chinh.add(s)

        stars_in_san_fang = set()
        for idx in [0, 4, 8, 6]:
            chinh = [s.split("(")[0] for s in la_so_dict["thap_nhi_cung"][idx % 12]["chinh_tinh"]]
            stars_in_san_fang.update(chinh)

        co_nguyet_dong_luong = sum(1 for s in ["Thiên Cơ", "Thái Âm", "Thiên Đồng", "Thiên Lương"] if s in stars_in_san_fang) >= 3
        sat_pha_tham = sum(1 for s in ["Thất Sát", "Phá Quân", "Tham Lang"] if s in stars_in_san_fang) >= 2
        tu_phu_dong_cung = "Tử Vi" in menh_stars and "Thiên Phủ" in menh_stars
        tu_vi_sat = "Tử Vi" in menh_stars and "Thất Sát" in menh_stars

        if co_nguyet_dong_luong:
            results.append({
                "sach": "Cốt Tủy Phú",
                "rule": "Cơ Nguyệt Đồng Lương tác lại nhân",
                "meaning": "Cách cục trí thức, công chức, thích hợp làm việc chuyên môn, nghiên cứu.",
            })

        if sat_pha_tham:
            results.append({
                "sach": "Cốt Tủy Phú",
                "rule": "Sát Phá Tham lang mãnh liệt, hành tẩu giang hồ",
                "meaning": "Cuộc đời phiêu bạt, biến động, khởi nghiệp từ tay trắng, hợp kinh doanh hoặc nghề tự do.",
            })

        if tu_phu_dong_cung:
            results.append({
                "sach": "Thái Vi Phú",
                "rule": "Tử Phủ đồng cung: phú quý song toàn",
                "meaning": "Phú quý song toàn, địa vị cao, tài chính vững.",
            })

        if tu_vi_sat and menh_cung["so_thu_tu"] in (2, 8):
            results.append({
                "sach": "Cốt Tủy Phú",
                "rule": "Tử Vi Thất Sát triều đẩu",
                "meaning": "Uy quyền tột đỉnh, võ chức hiển hách. Hợp với quân đội, công an, quản lý cấp cao.",
            })

        return results
